package railways;

public class Passenger {
	String passengerName;
	int age;
	String gender;
	int bookingID;
	//constructor of passenger class
	Passenger(String passengerName, int age, String gender, int bookingID){
		this.passengerName=passengerName;
		this.age=age;
		this.gender=gender;
		this.bookingID = bookingID;
	}
}

